package com.emodou.domain;

/**
 * 单元实体类
 * @author woody
 *
 */
public class EmodouUnit {
	
	private int id;
	private String bookid;
	private String unitid;
	private String unitname;
	private String uniticon;
	private String unitdes;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getBookid() {
		return bookid;
	}
	public void setBookid(String bookid) {
		this.bookid = bookid;
	}
	public String getUnitid() {
		return unitid;
	}
	public void setUnitid(String unitid) {
		this.unitid = unitid;
	}
	public String getUnitname() {
		return unitname;
	}
	public void setUnitname(String unitname) {
		this.unitname = unitname;
	}
	public String getUniticon() {
		return uniticon;
	}
	public void setUniticon(String uniticon) {
		this.uniticon = uniticon;
	}
	public String getUnitdes() {
		return unitdes;
	}
	public void setUnitdes(String unitdes) {
		this.unitdes = unitdes;
	}
}
