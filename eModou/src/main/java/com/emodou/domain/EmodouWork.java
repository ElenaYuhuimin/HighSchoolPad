package com.emodou.domain;

import android.R.integer;

/*
 * 作业数据库
 */
public class EmodouWork {
	
	private int id;
	
	private String userid;
	private String classroomid;
	
	private String workid;
	private String workname;
	private String starttime;//布置时间//暂时删掉了
	private String addtime;//开始时间
	private String endtime;//截止时间
	private String total;//作业项总数
	private String finished;//已完成作业项数量
	
	private String ifcheck;//这次作业是否查看过
	private String ifdone;//作业是否已做完
	private String ifcutoff; //作业是否过期
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getClassroomid() {
		return classroomid;
	}
	public void setClassroomid(String classroomid) {
		this.classroomid = classroomid;
	}
	public String getWorkid() {
		return workid;
	}
	public void setWorkid(String workid) {
		this.workid = workid;
	}
	public String getWorkname() {
		return workname;
	}
	public void setWorkname(String workname) {
		this.workname = workname;
	}
	public String getStarttime() {
		return starttime;
	}
	public void setStarttime(String starttime) {
		this.starttime = starttime;
	}
	public String getAddtime() {
		return addtime;
	}
	public void setAddtime(String addtime) {
		this.addtime = addtime;
	}
	public String getEndtime() {
		return endtime;
	}
	public void setEndtime(String endtime) {
		this.endtime = endtime;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getFinished() {
		return finished;
	}
	public void setFinished(String finished) {
		this.finished = finished;
	}
	public String getIfcheck() {
		return ifcheck;
	}
	public void setIfcheck(String ifcheck) {
		this.ifcheck = ifcheck;
	}
	public String getIfdone() {
		return ifdone;
	}
	public void setIfdone(String ifdone) {
		this.ifdone = ifdone;
	}
	public String getIfcutoff() {
		return ifcutoff;
	}
	public void setIfcutoff(String ifcutoff) {
		this.ifcutoff = ifcutoff;
	}
}
