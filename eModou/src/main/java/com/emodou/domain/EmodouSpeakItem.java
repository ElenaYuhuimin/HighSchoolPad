package com.emodou.domain;


public class EmodouSpeakItem {
	private EmodouSpeakBean speakbean;
	private int state; //是否选中  1选中 2 未选中
	private int play;  //是否正在播放 1未播放  2 播放系统 3 播放录音
	private float progress; // 播放进度
	private float myVoiceProgres; //设置我的音频播放进度
	private String mp3url; 
	private int score;//该课当所有都录音完打完分后，算出的平均分
	private String myVoiceUrl;
	public String getMyVoiceUrl() {
		return myVoiceUrl;
	}
	public void setMyVoiceUrl(String myVoiceUrl) {
		this.myVoiceUrl = myVoiceUrl;
	}
	public EmodouSpeakBean getSpeakbean() {
		return speakbean;
	}
	public void setSpeakbean(EmodouSpeakBean speakbean) {
		this.speakbean = speakbean;
	}
	public float getMyVoiceProgres() {
		return myVoiceProgres;
	}
	public void setMyVoiceProgres(float myVoiceProgres) {
		this.myVoiceProgres = myVoiceProgres;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	public int getPlay() {
		return play;
	}
	public void setPlay(int play) {
		this.play = play;
	}
	public float getProgress() {
		return progress;
	}
	public void setProgress(float progress) {
		this.progress = progress;
	}
	public String getMp3url() {
		return mp3url;
	}
	public void setMp3url(String mp3url) {
		this.mp3url = mp3url;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	
	
}
