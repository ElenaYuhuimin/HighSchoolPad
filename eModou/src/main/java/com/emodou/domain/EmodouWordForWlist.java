package com.emodou.domain;





//这个类是为了WordwlistActivity建立的

public class EmodouWordForWlist {


	private int id;
	private String userid;
	private String wordname;
	//增加此三个字段的目的是因为现在点击某一课进去查看单词界面中多了释义一项，若从EmodouWordInfo里面取，则没有学习状态等
	private String wordmean;
	private String voice;
    private String local_voice;
    //记录当前的翻转状态
    private boolean flipState;//正面在上为true，反面在上为false
	
	
	private String classid;
	private String isAddToNewWordsBood;
	private int learnState;//-1陌生 0还未学习 1模糊 2熟悉
	private int reviewState;//-1需要复习 0未学习 1学习过 2复习过（-1的状态还未用到）
	private long lastStateTime;
	private int lastState;
	
	public String getWordname() {
		return wordname;
	}
	public void setWordname(String wordname) {
		this.wordname = wordname;
	}
	public boolean getFlipState() {
		return flipState;
	}
	public void setFlipState(boolean flipState) {
		this.flipState = flipState;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getIsAddToNewWordsBood() {
		return isAddToNewWordsBood;
	}
	public void setIsAddToNewWordsBood(String isAddToNewWordsBood) {
		this.isAddToNewWordsBood = isAddToNewWordsBood;
	}
	public String getClassid() {
		return classid;
	}
	public void setClassid(String classid) {
		this.classid = classid;
	}
	public int getLearnState() {
		return learnState;
	}
	public void setLearnState(int learnState) {
		this.learnState = learnState;
	}
	public int getReviewState() {
		return reviewState;
	}
	public String getVoice() {
		return voice;
	}
	
	public void setVoice(String voice) {
		this.voice = voice;
	}
	public String getLocal_voice() {
		return local_voice;
	}
	public void setLocal_voice(String local_voice) {
		this.local_voice = local_voice;
	}
	public void setReviewState(int reviewState) {
		this.reviewState = reviewState;
	}
	public String getWordmean() {
		return wordmean;
	}
	public void setWordmean(String wordmean) {
		this.wordmean = wordmean;
	}
	public long getLastStateTime() {
		return lastStateTime;
	}
	public void setLastStateTime(long lastStateTime) {
		this.lastStateTime = lastStateTime;
	}
	public int getLastState() {
		return lastState;
	}
	public void setLastState(int lastState) {
		this.lastState = lastState;
	}
	
	

}
