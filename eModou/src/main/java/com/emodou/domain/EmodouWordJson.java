package com.emodou.domain;

import java.util.List;

/**
 * Created by woody on 2015/8/25.
 */
public class EmodouWordJson {
    String Bookid;
    String Type;
    List<EmodouWordUnit> Unit;

    @Override
    public String toString() {
        return "WordJson{" +
                "Bookid='" + Bookid + '\'' +
                ", Type='" + Type + '\'' +
                ", Unit=" + Unit +
                '}';
    }

    public String getBookid() {
        return Bookid;
    }

    public void setBookid(String bookid) {
        Bookid = bookid;
    }

    public String getType() {
        return Type;
    }

    public void setType(String type) {
        Type = type;
    }

    public List<EmodouWordUnit> getUnit() {
        return Unit;
    }

    public void setUnit(List<EmodouWordUnit> unit) {
        Unit = unit;
    }
}
